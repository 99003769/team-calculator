#include "area.h"


int rect_area(int length,int width)
{
     int area;
     area=length*width;
     return area;
}
int square_area(int length)
{
     int area1;
     area=length*length;
     return area1;
}
int triangle_area(int length,int width)
{
     int area2;
     area=0.5*length*width;
     return area2;
}
